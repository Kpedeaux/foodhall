#!/usr/bin/env node
// One-shot migration: introduce date-versioned vendor_terms + vendors.started_date.
//
// Run BEFORE deploying the date-versioned plan feature. The new code reads from
// vendor_terms; without this migration the SELECT will reference a missing table.
//
// USAGE:
//   set DATABASE_URL=postgresql://doadmin:PASSWORD@HOST:25060/foodhall?sslmode=require
//   node scripts/add-vendor-terms.mjs
//
// Idempotent: safe to run multiple times.

import postgres from 'postgres';

const url = process.env.DATABASE_URL;
if (!url) {
  console.error('ERROR: DATABASE_URL is not set. See script header.');
  process.exit(1);
}

const dbName = (() => { try { return new URL(url).pathname.replace(/^\//, ''); } catch { return '(unparseable)'; } })();
console.log(`Connecting to database: ${dbName}`);
const sql = postgres(url, { ssl: 'require', connect_timeout: 10, max: 1 });

try {
  // 1. Add started_date column to vendors.
  await sql.unsafe(`ALTER TABLE vendors ADD COLUMN IF NOT EXISTS started_date DATE;`);
  console.log('✓ vendors.started_date column added (or already present).');

  // 2. Create vendor_terms table.
  await sql.unsafe(`
    CREATE TABLE IF NOT EXISTS vendor_terms (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      vendor_id INTEGER NOT NULL REFERENCES vendors(id) ON DELETE CASCADE,
      effective_from DATE NOT NULL,
      plan_type TEXT NOT NULL CHECK (plan_type IN ('FLAT', 'STANDARD', 'WEEKLY')),
      percentage_rate DOUBLE PRECISION NOT NULL,
      daily_base_rent DOUBLE PRECISION NOT NULL,
      delivery_fee_rate DOUBLE PRECISION NOT NULL,
      service_charge_rate DOUBLE PRECISION NOT NULL,
      weekly_minimum DOUBLE PRECISION NOT NULL,
      linen_charge DOUBLE PRECISION NOT NULL,
      created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
      created_by INTEGER REFERENCES users(id),
      UNIQUE(vendor_id, effective_from)
    );
  `);
  await sql.unsafe(`
    CREATE INDEX IF NOT EXISTS idx_vendor_terms_vendor_effective
      ON vendor_terms(vendor_id, effective_from DESC);
  `);
  console.log('✓ vendor_terms table + index created.');

  // 3. Backfill: insert one vendor_terms row per existing vendor, snapshotting
  // its current plan fields. effective_from = '2020-01-01' guarantees this
  // row is the active plan for every week we'd ever calculate.
  const backfilled = await sql`
    INSERT INTO vendor_terms
      (vendor_id, effective_from, plan_type, percentage_rate, daily_base_rent,
       delivery_fee_rate, service_charge_rate, weekly_minimum, linen_charge)
    SELECT
      id, '2020-01-01'::date, plan_type, percentage_rate, daily_base_rent,
      delivery_fee_rate, service_charge_rate, weekly_minimum, linen_charge
    FROM vendors
    ON CONFLICT (vendor_id, effective_from) DO NOTHING
    RETURNING vendor_id
  `;
  console.log(`✓ Backfilled vendor_terms for ${backfilled.length} vendors.`);

  // 4. Grants.
  await sql.unsafe(`GRANT SELECT, INSERT, UPDATE, DELETE ON vendor_terms TO foodhall_app;`);
  console.log('✓ GRANTs reaffirmed for foodhall_app.');

  // 5. Verify.
  const [{ vendor_count }] = await sql`SELECT COUNT(*)::int AS vendor_count FROM vendors`;
  const [{ terms_count }] = await sql`SELECT COUNT(*)::int AS terms_count FROM vendor_terms`;
  console.log(`Verification: ${vendor_count} vendors, ${terms_count} vendor_terms rows.`);
  if (terms_count < vendor_count) {
    console.warn('WARNING: fewer vendor_terms than vendors — some vendors lack a baseline plan row.');
  }

  console.log('\nMigration complete. Safe to deploy the date-versioned plan code now.');
} catch (err) {
  console.error('Migration failed:', err.message);
  process.exit(2);
} finally {
  await sql.end({ timeout: 5 });
}
